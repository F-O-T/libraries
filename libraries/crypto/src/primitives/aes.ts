// Galois field multiplication in GF(2^8) with irreducible polynomial 0x11b
function gmul(a: number, b: number): number {
   let p = 0;
   for (let i = 0; i < 8; i++) {
      if (b & 1) p ^= a;
      const hiBit = a & 0x80;
      a = (a << 1) & 0xff;
      if (hiBit) a ^= 0x1b;
      b >>= 1;
   }
   return p;
}

// Round constant
const RCON = new Uint8Array([
   0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36,
]);

/**
 * Expand AES key into round keys.
 * key: 16, 24, or 32 bytes
 * Returns Uint8Array of (Nk+6+1)*16 bytes (all round keys concatenated)
 */
function expandKey(key: Uint8Array): Uint8Array {
   const nk = key.length / 4; // 4, 6, or 8
   const nr = nk + 6; // 10, 12, or 14 rounds
   const nWords = (nr + 1) * 4;

   const w = new Uint8Array(nWords * 4);
   w.set(key);

   for (let i = nk; i < nWords; i++) {
      const t = w.subarray((i - 1) * 4, i * 4);
      const temp = new Uint8Array(4);
      temp.set(t);

      if (i % nk === 0) {
         // RotWord + SubWord + Rcon
         const t0 = temp[0]!;
         temp[0] = SBOX[temp[1]!]! ^ RCON[i / nk - 1]!;
         temp[1] = SBOX[temp[2]!]!;
         temp[2] = SBOX[temp[3]!]!;
         temp[3] = SBOX[t0]!;
      } else if (nk > 6 && i % nk === 4) {
         // SubWord only for AES-256
         temp[0] = SBOX[temp[0]!]!;
         temp[1] = SBOX[temp[1]!]!;
         temp[2] = SBOX[temp[2]!]!;
         temp[3] = SBOX[temp[3]!]!;
      }

      const base = (i - nk) * 4;
      w[i * 4] = w[base]! ^ temp[0]!;
      w[i * 4 + 1] = w[base + 1]! ^ temp[1]!;
      w[i * 4 + 2] = w[base + 2]! ^ temp[2]!;
      w[i * 4 + 3] = w[base + 3]! ^ temp[3]!;
   }

   return w;
}

/** XOR state with round key (in-place) */
function addRoundKey(state: Uint8Array, rk: Uint8Array, round: number): void {
   const offset = round * 16;
   for (let i = 0; i < 16; i++) {
      state[i]! ^= rk[offset + i]!;
   }
}

/** Apply S-box to each byte */
function subBytes(state: Uint8Array): void {
   for (let i = 0; i < 16; i++) {
      state[i] = SBOX[state[i]!]!;
   }
}

/** Apply inverse S-box to each byte */
function invSubBytes(state: Uint8Array): void {
   for (let i = 0; i < 16; i++) {
      state[i] = SBOX_INV[state[i]!]!;
   }
}

/** Shift rows left */
function shiftRows(state: Uint8Array): void {
   let t: number;
   // Row 1: shift 1 left
   t = state[1]!;
   state[1] = state[5]!;
   state[5] = state[9]!;
   state[9] = state[13]!;
   state[13] = t;
   // Row 2: shift 2 left
   t = state[2]!;
   state[2] = state[10]!;
   state[10] = t;
   t = state[6]!;
   state[6] = state[14]!;
   state[14] = t;
   // Row 3: shift 3 left (= 1 right)
   t = state[15]!;
   state[15] = state[11]!;
   state[11] = state[7]!;
   state[7] = state[3]!;
   state[3] = t;
}

/** Inverse shift rows (right) */
function invShiftRows(state: Uint8Array): void {
   let t: number;
   // Row 1: shift 1 right
   t = state[13]!;
   state[13] = state[9]!;
   state[9] = state[5]!;
   state[5] = state[1]!;
   state[1] = t;
   // Row 2: shift 2 right
   t = state[2]!;
   state[2] = state[10]!;
   state[10] = t;
   t = state[6]!;
   state[6] = state[14]!;
   state[14] = t;
   // Row 3: shift 3 right (= 1 left)
   t = state[3]!;
   state[3] = state[7]!;
   state[7] = state[11]!;
   state[11] = state[15]!;
   state[15] = t;
}

/** Mix columns */
function mixColumns(state: Uint8Array): void {
   for (let col = 0; col < 4; col++) {
      const s0 = state[col * 4]!;
      const s1 = state[col * 4 + 1]!;
      const s2 = state[col * 4 + 2]!;
      const s3 = state[col * 4 + 3]!;
      state[col * 4] = gmul(0x02, s0) ^ gmul(0x03, s1) ^ s2 ^ s3;
      state[col * 4 + 1] = s0 ^ gmul(0x02, s1) ^ gmul(0x03, s2) ^ s3;
      state[col * 4 + 2] = s0 ^ s1 ^ gmul(0x02, s2) ^ gmul(0x03, s3);
      state[col * 4 + 3] = gmul(0x03, s0) ^ s1 ^ s2 ^ gmul(0x02, s3);
   }
}

/** Inverse mix columns */
function invMixColumns(state: Uint8Array): void {
   for (let col = 0; col < 4; col++) {
      const s0 = state[col * 4]!;
      const s1 = state[col * 4 + 1]!;
      const s2 = state[col * 4 + 2]!;
      const s3 = state[col * 4 + 3]!;
      state[col * 4] =
         gmul(0x0e, s0) ^ gmul(0x0b, s1) ^ gmul(0x0d, s2) ^ gmul(0x09, s3);
      state[col * 4 + 1] =
         gmul(0x09, s0) ^ gmul(0x0e, s1) ^ gmul(0x0b, s2) ^ gmul(0x0d, s3);
      state[col * 4 + 2] =
         gmul(0x0d, s0) ^ gmul(0x09, s1) ^ gmul(0x0e, s2) ^ gmul(0x0b, s3);
      state[col * 4 + 3] =
         gmul(0x0b, s0) ^ gmul(0x0d, s1) ^ gmul(0x09, s2) ^ gmul(0x0e, s3);
   }
}

/** Encrypt a single 16-byte block in place using the expanded round key */
function aesEncryptBlock(block: Uint8Array, rk: Uint8Array, nr: number): void {
   const state = new Uint8Array(block);
   addRoundKey(state, rk, 0);
   for (let round = 1; round < nr; round++) {
      subBytes(state);
      shiftRows(state);
      mixColumns(state);
      addRoundKey(state, rk, round);
   }
   subBytes(state);
   shiftRows(state);
   addRoundKey(state, rk, nr);
   block.set(state);
}

/** Decrypt a single 16-byte block in place using the expanded round key */
function aesDecryptBlock(block: Uint8Array, rk: Uint8Array, nr: number): void {
   const state = new Uint8Array(block);
   addRoundKey(state, rk, nr);
   for (let round = nr - 1; round > 0; round--) {
      invShiftRows(state);
      invSubBytes(state);
      addRoundKey(state, rk, round);
      invMixColumns(state);
   }
   invShiftRows(state);
   invSubBytes(state);
   addRoundKey(state, rk, 0);
   block.set(state);
}

/**
 * Decrypt data encrypted with AES-CBC + PKCS#7 padding.
 *
 * @param key - 16, 24, or 32 bytes
 * @param iv  - 16 bytes
 * @param ciphertext - Must be a multiple of 16 bytes
 */
export function aesCbcDecrypt(
   key: Uint8Array,
   iv: Uint8Array,
   ciphertext: Uint8Array,
): Uint8Array {
   if (key.length !== 16 && key.length !== 24 && key.length !== 32) {
      throw new Error(`AES: invalid key length ${key.length}`);
   }
   if (iv.length !== 16) throw new Error("AES-CBC: IV must be 16 bytes");
   if (ciphertext.length % 16 !== 0)
      throw new Error("AES-CBC: ciphertext must be multiple of 16 bytes");

   const nr = key.length / 4 + 6; // 10, 12, or 14
   const rk = expandKey(key);

   const plaintext = new Uint8Array(ciphertext.length);
   let prev = iv;

   for (let i = 0; i < ciphertext.length; i += 16) {
      const block = new Uint8Array(ciphertext.subarray(i, i + 16));
      const ct = new Uint8Array(block); // save ciphertext block for next IV
      aesDecryptBlock(block, rk, nr);
      // XOR with previous ciphertext block
      for (let j = 0; j < 16; j++) {
         plaintext[i + j] = block[j]! ^ prev[j]!;
      }
      prev = ct;
   }

   // Remove PKCS#7 padding
   return removePkcs7Padding(plaintext);
}

function removePkcs7Padding(data: Uint8Array): Uint8Array {
   if (data.length === 0) return data;
   const pad = data[data.length - 1]!;
   if (pad === 0 || pad > 16) throw new Error("Invalid PKCS#7 padding");
   for (let i = data.length - pad; i < data.length; i++) {
      if (data[i] !== pad) throw new Error("Invalid PKCS#7 padding");
   }
   return data.subarray(0, data.length - pad);
}
